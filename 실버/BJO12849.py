for i in range(n):
    dp = [0,0,0,0,0,0,0,0]
    dp[0] = li[1] + li[2]
    dp[1] = li[0] + li[2] + li[3]
    dp[2] = li[0] + li[1] + li[3] + li[4]
    dp[3] = li[1] + li[2] + li[4] + li[5]
    dp[4] = li[2] + li[3] + li[5] + li[6]
    dp[5] = li[3] + li[4] + li[7]
    dp[6] = li[4] + li[7]
    dp[7] = li[5] + li[6]
    for i in range(8):
        if dp[i] > 1000000007:
            dp[i] = dp[i]%1000000007
    li = dp
